            dt.setGVHD(txtGVHD.getText());
            dt.setTenSVThucHien(txtSV.getText());
            dt.setKhoa(Integer.valueOf(txtKhoa.getText()));
            dt.setCongNgheSuDung(txtCongNghe.getText());
            dt.setTienDo(txtTienDo.getText());
