    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{

            {
                String id=txtMaDeTai.getText();
                DeTaiDao dao= new DeTaiDao();
                DeTai nt=dao.timkiem(id);
                if(nt !=null)
                {
                    txtMaDeTai.setText(nt.getMaDeTai());
                    txtTenDeTai.setText(nt.getTenDeTai());
                    txtGVHD.setText(nt.getGVHD());
                    txtSV.setText(nt.getTenSVThucHien());
                    String khoa=String.valueOf(nt.getKhoa());
                    txtKhoa.setText(khoa);
                    String thu=String.valueOf(nt.getCongNgheSuDung());
                    txtCongNghe.setText(thu);
                    txtTienDo.setText(nt.getTienDo());
                }
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        try{
            int row=tbl.getSelectedRow();
            if(row>=0)
            {
                String id=(String) tbl.getValueAt(row, 0);
                DeTaiDao dao= new DeTaiDao();
                DeTai nt=dao.timkiem(id);
                if(nt !=null)
                {
                    txtMaDeTai.setText(nt.getMaDeTai());
                    txtTenDeTai.setText(nt.getTenDeTai());
                    txtGVHD.setText(nt.getGVHD());
                    txtSV.setText(nt.getTenSVThucHien());
                    String sokm=String.valueOf(nt.getKhoa());
                    txtKhoa.setText(sokm);
                    String thu=String.valueOf(nt.getCongNgheSuDung());
                    txtCongNghe.setText(thu);
                    txtTienDo.setText(nt.getTienDo());
                }
            }
        }
        catch(Exception e)
        {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblMouseClicked

